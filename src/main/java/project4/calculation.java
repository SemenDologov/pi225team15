package project4;

public abstract class calculation {

	   private int length;
	   private int width;

	   public abstract void Calc();

	   public int getLength() {
	       return length;
	   }

	   public void setLength(int length) {
	       this.length = length;
	   }

	   public int getWidth() {
	       return width;
	   }

	   public void setWidth(int width) {
	       this.width = width;
	   }
}

